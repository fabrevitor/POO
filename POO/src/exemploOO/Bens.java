package exemploOO;

public class Bens {
	
	private Carro carro;
	private Moto moto;
	private Casa casa;
	private Apto apto;
	
	public Carro getCarro() {
		return carro;
	}
	public void setCarro(Carro carro) {
		this.carro = carro;
	}
	public Moto getMoto() {
		return moto;
	}
	public void setMoto(Moto moto) {
		this.moto = moto;
	}
	public Casa getCasa() {
		return casa;
	}
	public void setCasa(Casa casa) {
		this.casa = casa;
	}
	public Apto getApto() {
		return apto;
	}
	public void setApto(Apto apto) {
		this.apto = apto;
	}
	
}

